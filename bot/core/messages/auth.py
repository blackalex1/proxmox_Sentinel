def get_vps_ssh_login_alert(ip, username, client_ip, auth_method, key_name, fingerprint, timestamp, security_warning_str, line):
    key_info = ""
    if auth_method == "publickey" and fingerprint:
        key_val = key_name or fingerprint
        key_info = f" (Ключ: <code>{key_val}</code>)"
    
    security_aside = ""
    if security_warning_str:
        security_aside = f"\n<blockquote>{security_warning_str}</blockquote>\n"

    raw_line = line.strip()

    return (
        f"<h1>🖥️ VPS SSH Security: {ip}</h1>\n"
        f"<hr/>\n\n"
        f"<h3>🟢 Успешный вход по SSH!</h3>\n\n"
        f"<table bordered striped>\n"
        f"  <tr>\n"
        f"    <th align=\"left\">Параметр</th>\n"
        f"    <th align=\"left\">Значение</th>\n"
        f"  </tr>\n"
        f"  <tr>\n"
        f"    <td><b>👤 Пользователь</b></td>\n"
        f"    <td><code>{username}</code></td>\n"
        f"  </tr>\n"
        f"  <tr>\n"
        f"    <td><b>🌐 IP-адрес</b></td>\n"
        f"    <td><code>{client_ip}</code></td>\n"
        f"  </tr>\n"
        f"  <tr>\n"
        f"    <td><b>🔑 Метод</b></td>\n"
        f"    <td><code>{auth_method}</code>{key_info}</td>\n"
        f"  </tr>\n"
        f"</table>\n\n"
        f"{security_aside}"
        f"<details>\n"
        f"  <summary>🔍 <b>Показать лог входа</b></summary>\n"
        f"  <pre><code>{raw_line}</code></pre>\n"
        f"</details>"
    )

